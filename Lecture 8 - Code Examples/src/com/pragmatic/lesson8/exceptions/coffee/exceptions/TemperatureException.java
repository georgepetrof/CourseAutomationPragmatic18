package com.pragmatic.lesson8.exceptions.coffee.exceptions;

public class TemperatureException extends Exception {

	public TemperatureException() {
		super();
	}

	public TemperatureException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TemperatureException(String arg0) {
		super(arg0);
	}

	public TemperatureException(Throwable arg0) {
		super(arg0);
	}

	
	
	
}
